//ARITMÉTICOS

const num1 = 5;
const num2 = 10;
const nome = '10'

// + (adição/conctenação)
console.log(num1 + num2)
console.log(num1 + nome)

// - / * (subtração, divisão e multiplicação)
console.log(num1 - num2);
console.log(num1 / num2);
console.log(num1 * num2);

// ** (potenciação)
console.log(num1 ** num2);

// % (resto da divisão)
console.log(num2 % num1);

//INCREMENTO
let contador = 1;
console.log(contador++) //pós fixado, o consle mostra o valor antes de adicionar
console.log(contador) //nesta linha o valor já foi adicionado, por isso aparece 2

